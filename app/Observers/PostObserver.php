    /**
     * Handle the Post "created" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        $user = User::where('id',$post->user_id)->with('readers')->first();
        $readers = $user->readers;
        foreach ($readers as $reader){
            SendMessageToReader::dispatch($reader->email,$post->title, $post->slug, $user->name, $post->image );
        }
    }

